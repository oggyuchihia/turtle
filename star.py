import turtle
turtle.Screen().bgcolor("blue")

object=turtle.Turtle()

object.forward(100)
object.left(120)

object.forward(100)

object.left(120)
object.forward(100)

object.penup()
object.right(150)

object.forward(50)

object.pendown()
object.right(90)

object.forward(100)
object.right(120)

object.forward(100)
object.right(120)
object.forward(100)

turtle.done()